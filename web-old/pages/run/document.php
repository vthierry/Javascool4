<h1> <span class="mw-headline" id="Utiliser_JavaScool"> Utiliser JavaScool </span></h1>
<ul><li> <i>En un clic</i>: Le logiciel et les documents des activités se chargent en <?php showLink('javascool.jar','un clic','file');?>, il suffit que ``Java soit installé´´ sur l'ordinateur.
<ul><li> Si Java n'est pas installé ou la version est trop ancienne cliquer <?php showLink('http://www.java.com/fr','ici','external'); ?>. N'importe quelle version relativement récente: <i>Java 1.5</i>, ou <i>Java 1.6</i>, .. suffit. Pas besoin de ``Jdk´´ (Java Developpement Kit). 
</li></ul>
</li></ul>
<ul><li> <i>Sans réseau</i>&nbsp;: Si les ordinateurs de travail ne sont pas sur le réseau il suffit de copier <?php showLink('javascool.jar','javascool.jar','file');?> sur une clé USB puis de la faire passer sur chaque ordinateur de travail (par exemple en le copiant sur le "bureau").
</li></ul>
<ul><li> <i>Sauver les fichiers</i>&nbsp;: Attention à bien sauvegarder les fichiers <i>dans un dossier de l'utilisateur</i> (sur le "bureau" ou dans le dossier "documents") sinon le système risque de se plaindre d'une tentative d'écriture illicite.
</li></ul>
