<p>Il faut que <?php showLink('http://www.java.com/fr/download','Java','external'); ?> soit installé sur l'ordinateur
</p>
<dl><dd> (n'importe quelle version "récente" de <?php showLink('http://www.java.com/fr/download','Java 1.5, 1.6','external'); ?> etc.. devrait marcher et Java est presque toujours installé par défaut).
</dd></dl>
<ul><li> Il suffit normallement de cliquer sur <?php showLink('javascool.jar','LANCEMENT','file');?> et de choisir <tt>Ouvrir avec . . <i>java</i></tt>.
</li></ul>
